name = "Felipe"
name2 = "Constanza"

if name == name2:
    print("Ambos tienen el mismo nombre")

else:
    print(f"{name} no se llama igual que {name2}")

age = 15
age2 = 15

if age == age2:
    print(f"{name} tiene la misma edad que {name2}")

elif age > age2:
    print(f"{name} es mayor que {name2}")

else:
    print(f"{name} es menor que {name2}")